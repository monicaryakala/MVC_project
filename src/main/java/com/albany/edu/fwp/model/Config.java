package com.albany.edu.fwp.model;



public class Config {
    private int configId;
    private String managerTime; 
    private String studentTime;
    
	public int getConfigId() {
		return configId;
	}
	public void setConfigId(int configId) {
		this.configId = configId;
	}
	
	public String getManagerTime() {
		return managerTime;
	}
	public void setManagerTime(String managerTime) {
		this.managerTime = managerTime;
	}
	
	public String getStudentTime() {
		return studentTime;
	}
	public void setStudentTime(String studentTime) {
		this.studentTime = studentTime;
	} 
	
	
}
